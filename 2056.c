//연월일 순으로 구성된 8자리의 날짜가 입력으로 주어진다.
//해당 날짜의 유효성을 판단한 후, 날짜가 유효하다면
//[그림1] 과 같이 ”YYYY / MM / DD”형식으로 출력하고,
//날짜가 유효하지 않을 경우, -1 을 출력하는 프로그램을 작성하라.
//연월일로 구성된 입력에서 월은 1~12 사이 값을 가져야 하며
//일은[표1] 과 같이, 1일 ~각각의 달에 해당하는 날짜까지의 값을 가질 수 있다.
//※ 2월의 경우, 28일인 경우만 고려한다. (윤년은 고려하지 않는다.)

//[입력]
//입력은 첫 줄에 총 테스트 케이스의 개수 T가 온다.
//다음 줄부터 각 테스트 케이스가 주어진다.

//[출력]
//테스트 케이스 t에 대한 결과는 “#t”을 찍고, 한 칸 띄고, 정답을 출력한다.
//(t는 테스트 케이스의 번호를 의미하며 1부터 시작한다.)

#include <stdio.h>

int monthChecker(int num) {
	if (num >= 1 && num <= 12) {
		return 1;
	}
	else {
		return 0;
	}
}

int monthGroupFinder(int month) {
	char months30[4] = { 4,6,9,11 };
	char months31[7] = { 1,3,5,7,8,10,12 };
	char months28[1] = { 2 };
	char monthCheck = monthChecker(month);

	if (monthCheck == 1) {
		for (int i = 0; i < 4; i++) {
			if (month == months30[i]) {
				return 30;
			}
		}

		for (int i = 0; i < 7; i++) {
			if (month == months31[i]) {
				return 31;
			}
		}

		return 28;
	}
	else {
		return 0;
	}
}

int dateChecker(int month, int date) {
	char monthGroup = monthGroupFinder(month);
	if (monthGroup == 30) {
		if (date >= 1 && date <= 30) {
			return 1;
		}
	}
	else if (monthGroup == 31) {
		if (date >= 1 && date <= 31) {
			return 1;
		}
	}
	else if (monthGroup == 28) {
		if (date >= 1 && date <= 28) {
			return 1;
		}
	}
	else {
		return 0;
	}
}

int formatter(int num) {
	int year, month, date;

	year = num / 10000;
	month = (num % 10000) / 100;
	date = (num % 10000) % 100;
	printf("%04d/%02d/%02d\n", year, month, date);

	return 1;
}

int main() {
	int tc, inNum;

	scanf("%d", &tc);

	for (int i = 0; i < tc; i++) {
		int year, month, date;

		scanf("%d", &inNum);

		year = inNum / 10000;
		month = (inNum % 10000) / 100;
		date = (inNum % 10000) % 100;

		char monthCheck = monthChecker(month);
		char dateCheck = dateChecker(month, date);

		if (monthCheck == 1 && dateCheck == 1) {
			printf("#%d ", i + 1);
			formatter(inNum);
		}
		else {
			printf("#%d -1\n", i + 1);
		}
	}

	return 0;
}